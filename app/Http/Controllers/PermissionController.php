     * Store a newly created resource in storage.
     */
    public function store(Request $request)
    {
        $request->validate([
            'name' => [
                'required',
                'string',
                'unique:permissions,name'
            ]
        ]);
        Permission::insert([
            'name' => $request->name,
        ]);
        return redirect('permission.index')->with('success','You Successfully Added A Permission');
    }

